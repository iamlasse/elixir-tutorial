  import Comeonin.Bcrypt, only: [checkpw: 2, dummy_checkpw: 0]

  @doc """
  Returns the list of users.

  ## Examples

      iex> list_users()
      [%User{}, ...]

  """
  def list_users do
    User
    |> Repo.all()
    |> Repo.preload(:credential)
  end

  @doc """
  Gets a single user.

  Raises `Ecto.NoResultsError` if the User does not exist.

  ## Examples

      iex> get_user!(123)
      %User{}

      iex> get_user!(456)
      ** (Ecto.NoResultsError)

  """
  def get_user!(id) do
    User
    |> Repo.get!(id)
    |> Repo.preload([:credential, :creator])
  end

  def find_user(id) do
    case Repo.get!(User, id) do
      %User{} = user -> {:ok, user}
      nil -> {:error, :no_resource}
    end
  end

  @doc """
  Creates a user.

  ## Examples

      iex> create_user(%{field: value})
      {:ok, %User{}}

      iex> create_user(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_user(attrs \\ %{}) do
    %User{}
    |> User.changeset(attrs)
    |> Ecto.Changeset.cast_assoc(:credential, with: &Credential.changeset/2)
    |> Repo.insert()
  end

  @doc """
  Updates a user.

  ## Examples

      iex> update_user(user, %{field: new_value})
      {:ok, %User{}}

      iex> update_user(user, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_user(%User{} = user, attrs) do
    user
    |> User.changeset(attrs)
    |> Ecto.Changeset.cast_assoc(:credential, with: &Credential.changeset/2)
    |> Repo.update()
  end

  @doc """
  Deletes a User.

  ## Examples

      iex> delete_user(user)
      {:ok, %User{}}

      iex> delete_user(user)
      {:error, %Ecto.Changeset{}}

  """
  def delete_user(%User{} = user) do
    Repo.delete(user)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking user changes.

  ## Examples

      iex> change_user(user)
      %Ecto.Changeset{source: %User{}}

  """
  def change_user(%User{} = user) do
    User.changeset(user, %{})
  end

  def verify_credentials(email, password) when is_binary(email) and is_binary(password) do
    with {:ok, user} <- authenticate_by_email(email), do: verify_password(password, user)
  end

  # Basic Authentication
  def authenticate_by_email(email) do
    query =
      from(
        u in User,
        inner_join: c in assoc(u, :credential),
        where: c.email == ^email,
        preload: [:credential, :creator]
      )

    case Repo.one(query) do
      %User{} = user ->
        {:ok, user}

      nil ->
        {:error, "No user with that email"}
    end
  end

  def verify_password(password, %User{} = user) when is_binary(password) do
    if checkpw(password, user.password_hash) do
      {:ok, user}
    else
      {:error, "Invalid password"}
    end
  end

  def get_users_map(entries) do
    query =
      from(
        u in User,
        where: u.id in ^entries,
        select: %{u.id => %{"username" => u.username}}
      )

    # |> Repo.preload(:credential)
    users =
      query
      |> Repo.all()
      |> Enum.into(%{})

    users
  end

